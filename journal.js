// Greet a user by name
function greet(name) {
  return "Hello, " + name + "!";
}

// Check if a number is even
// This function takes a number as input and returns true if it is even
function isEven(number) {
  return number % 2 === 0;
}

// Double a number
// This function takes a number as input and returns the number multiplied by 2
function double(number) {
  return number * 2;
}

// Check if a number is divisible by 3
// This function takes a number as input and returns true if it is divisible by 3,
function divisibleByThree(number) {
  return number % 3 === 0;
}

// Convert a string to uppercase
// This function takes a string as input and returns it in uppercase
function convertToUpperCase(str) {
  return str.toUpperCase();
}

// Calculate the area of a rectangle
// given its length and width
function calculateArea(length, width) {
  return length * width;
}
